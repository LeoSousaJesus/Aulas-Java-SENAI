        try {
            double valorA = Double.parseDouble(txtA.getText());
            double valorB = Double.parseDouble(txtB.getText());
            double resultado = mat.soma(valorA, valorB);
            lblResultado.setText(Double.toString(resultado));
        } catch (NumberFormatException e) {
            lblResultado.setText("Erro!");
        }
